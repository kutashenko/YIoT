#include <iostream>

#include "helpers/timer.h"
#include "helpers/command.h"

#include "commands/wifi-cred.h"

static KSTimer _processingDelayer;
static const auto kDelayMs = std::chrono::milliseconds(200);

//-----------------------------------------------------------------------------
vs_status_e
ks_snap_cfg_wifi_cb(const vs_cfg_wifi_configuration_t *configuration) {
    CHECK_NOT_ZERO_RET(configuration, VS_CODE_ERR_NULLPTR_ARGUMENT);

    printf("ssid: %s\n", configuration->ssid);
    printf("pass: %s\n", configuration->pass);

    bool res = _processingDelayer.add(kDelayMs, []() -> void {
        Command cmd;
        cmd.Command = "bash -c \"ls -lah \"";
        cmd.execute();

        std::cout << cmd.StdOut;
        std::cerr << cmd.StdErr;
        std::cout << "Exit Status: " << cmd.ExitStatus << "\n";
    });

    return res ? VS_CODE_OK : VS_CODE_ERR_QUEUE;
}

